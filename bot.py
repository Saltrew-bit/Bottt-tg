@dp.message()
async def process_message(msg: types.Message):
    user_id = msg.from_user.id
    if user_id not in ads_data:
        return

    step = ads_data[user_id]["step"]
    ad = ads_data[user_id]["data"]

    if step == 1:
        ad["model"] = msg.text
        ads_data[user_id]["step"] = 2
        await msg.answer("–í–≤–µ–¥–∏—Ç–µ –≥–æ–¥ –≤—ã–ø—É—Å–∫–∞ –∞–≤—Ç–æ–º–æ–±–∏–ª—è:")

    elif step == 2:
        if not msg.text.isdigit():
            await msg.answer("‚ùå –í–≤–µ–¥–∏—Ç–µ –≥–æ–¥ –≤—ã–ø—É—Å–∫–∞ —Ü–∏—Ñ—Ä–∞–º–∏, –Ω–∞–ø—Ä–∏–º–µ—Ä: 2015")
            return
        ad["year"] = msg.text
        ads_data[user_id]["step"] = 3
        await msg.answer("–í–≤–µ–¥–∏—Ç–µ —Ü–µ–Ω—É –∞–≤—Ç–æ–º–æ–±–∏–ª—è (‚ÇΩ):")

    elif step == 3:
        if not msg.text.replace(" ", "").isdigit():
            await msg.answer("‚ùå –í–≤–µ–¥–∏—Ç–µ —Ü–µ–Ω—É —Ü–∏—Ñ—Ä–∞–º–∏, –Ω–∞–ø—Ä–∏–º–µ—Ä: 350000")
            return
        ad["price"] = msg.text
        ads_data[user_id]["step"] = 4
        await msg.answer("–í–≤–µ–¥–∏—Ç–µ –ø—Ä–æ–±–µ–≥ –∞–≤—Ç–æ–º–æ–±–∏–ª—è (–∫–º):")

    elif step == 4:
        if not msg.text.replace(" ", "").isdigit():
            await msg.answer("‚ùå –í–≤–µ–¥–∏—Ç–µ –ø—Ä–æ–±–µ–≥ —Ü–∏—Ñ—Ä–∞–º–∏, –Ω–∞–ø—Ä–∏–º–µ—Ä: 120000")
            return
        ad["mileage"] = msg.text
        ads_data[user_id]["step"] = 5
        await msg.answer("–î–æ–±–∞–≤—å—Ç–µ –æ–ø–∏—Å–∞–Ω–∏–µ –∞–≤—Ç–æ–º–æ–±–∏–ª—è:")

    elif step == 5:
        ad["description"] = msg.text
        ads_data[user_id]["step"] = 6
        await msg.answer("–û—Ç–ø—Ä–∞–≤—å—Ç–µ —Ñ–æ—Ç–æ –∞–≤—Ç–æ–º–æ–±–∏–ª—è (–¥–æ 10 —à—Ç.). –ö–æ–≥–¥–∞ –∑–∞–∫–æ–Ω—á–∏—Ç–µ, –Ω–∞–ø–∏—à–∏—Ç–µ '—Å—Ç–æ–ø'.")

    elif step == 6:
        if msg.photo:
            ad.setdefault("photos", []).append(msg.photo[-1].file_id)
            if len(ad["photos"]) < 10:
                await msg.answer(f"–§–æ—Ç–æ –ø—Ä–∏–Ω—è—Ç–æ ({len(ad['photos'])}/10). –ú–æ–∂–µ—Ç–µ –ø—Ä–∏—Å–ª–∞—Ç—å –µ—â—ë –∏–ª–∏ –Ω–∞–ø–∏—à–∏—Ç–µ '—Å—Ç–æ–ø'.")
            else:
                ads_data[user_id]["step"] = 7
                await msg.answer("–§–æ—Ç–æ –∑–∞–≤–µ—Ä—à–µ–Ω—ã. –í–≤–µ–¥–∏—Ç–µ –∫–æ–Ω—Ç–∞–∫—Ç:")
        elif msg.text.lower() == "—Å—Ç–æ–ø":
            ads_data[user_id]["step"] = 7
            await msg.answer("–§–æ—Ç–æ –∑–∞–≤–µ—Ä—à–µ–Ω—ã. –í–≤–µ–¥–∏—Ç–µ –∫–æ–Ω—Ç–∞–∫—Ç:")
        else:
            await msg.answer("–û—Ç–ø—Ä–∞–≤—å—Ç–µ —Ñ–æ—Ç–æ –∏–ª–∏ –Ω–∞–ø–∏—à–∏—Ç–µ '—Å—Ç–æ–ø'.")

    elif step == 7:
        ad["contact"] = msg.text
        ads_data[user_id]["step"] = 8

        text = (
            f"–ù–æ–≤–æ–µ –æ–±—ä—è–≤–ª–µ–Ω–∏–µ –æ—Ç {msg.from_user.full_name}:\n\n"
            f"üöó –ú–æ–¥–µ–ª—å: {ad['model']}\n"
            f"üìÖ –ì–æ–¥ –≤—ã–ø—É—Å–∫–∞: {ad['year']}\n"
            f"üí∞ –¶–µ–Ω–∞: {ad['price']} ‚ÇΩ\n"
            f"üìè –ü—Ä–æ–±–µ–≥: {ad['mileage']} –∫–º\n"
            f"üìù –û–ø–∏—Å–∞–Ω–∏–µ: {ad['description']}\n"
            f"üìû –ö–æ–Ω—Ç–∞–∫—Ç: {ad['contact']}"
        )

        media = [InputMediaPhoto(pid) for pid in ad.get("photos", [])]
        pending_ads[user_id] = ad

        if media:
            await bot.send_media_group(ADMIN_ID, media)

        keyboard = InlineKeyboardMarkup(inline_keyboard=[
            [InlineKeyboardButton("‚úÖ –û–ø—É–±–ª–∏–∫–æ–≤–∞—Ç—å –≤ –∫–∞–Ω–∞–ª", callback_data=f"publish_{user_id}")],
            [InlineKeyboardButton("‚ùå –£–¥–∞–ª–∏—Ç—å –æ–±—ä—è–≤–ª–µ–Ω–∏–µ", callback_data=f"delete_{user_id}")]
        ])
        await bot.send_message(ADMIN_ID, text, reply_markup=keyboard)
        await msg.answer("‚úÖ –í–∞—à–µ –æ–±—ä—è–≤–ª–µ–Ω–∏–µ –ø—Ä–∏–Ω—è—Ç–æ –∏ –æ—Ç–ø—Ä–∞–≤–ª–µ–Ω–æ –Ω–∞ –º–æ–¥–µ—Ä–∞—Ü–∏—é!")
        del ads_data[user_id]
